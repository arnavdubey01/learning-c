#include<stdio.h>

int main(){
    int n = 6;
    for (int i = 0; i < n; i++){
        printf("The value of i is %d\n", i);
    }
    return 0;
}


// syntax:
// for (initialize; test; increment or decrement){...}

// basically,
// initialize:          setting up the starting value.
// test:                Condition that is checked.
// increment/decrement: Update in the starting value(increment or decrement)

#include<stdio.h>

int main(){
    int a = 1;

    float b = 1.34;
    
    char c = 'a'; // always typed in single quotes
    
    printf("The value of a is %d\n", a);
    printf("The value of a is %f\n", b);
    printf("The value of a is %c\n", c);

    return 0; 
}

// %d is used for integer
// %f is used for float
// %c is used for character


// 


// Never forget the semicolon

// int a;
// a = 1;   is same as: int a = 1;


// We have constants:
// 1. Integer Constant → 1,6,7,9 
// 2. Real Constant → 322.1, 2.5 ,7.0 
// 3. Character Constant → ‘a’, ‘$’, ‘@’ (must be enclosed within single quotes)  
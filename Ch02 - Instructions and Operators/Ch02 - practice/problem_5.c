// 5. 3.0 + 1 will be: 
// a. Integer. 
// b. Floating point number. 
// c. Character.

#include<stdio.h>

int main(){
    float n = 3.0 + 1;
    printf("%f", n);
    return 0;
}

// The result of any calculation between floating point and an integer is a floating point, so the answer to this question is, floating point.